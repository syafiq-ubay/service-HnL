    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        if (validasiInput() == true) {
            if(evt.getSource() == btn_add){
                String kodeOnderdil = t_kodeOnderdil.getText();
                int jmh = Integer.parseInt(t_jumlah.getText());
                
                if(cekStok(kodeOnderdil, jmh)){
                    
                    DefaultTableModel tb = (DefaultTableModel) tbl_sementara.getModel();
                    tb.addRow(new Object[]{
                        t_kodeOnderdil.getText(),
                        t_namaOnderdil.getText(),
                        t_harga.getText(),
                        t_jumlah.getText(),
                        t_subTotal.getText()
                    });

                    JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan!");
                }else{
                    JOptionPane.showMessageDialog(this, "Jumlah Onderdil melebihi stok yang tersedia!", "Peringatan", JOptionPane.WARNING_MESSAGE);

                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan!");
        }     
        total();
    }//GEN-LAST:event_btn_addActionPerformed

