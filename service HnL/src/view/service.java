    private void simpanData() {
            String kode = t_kodeService.getText();
            String idMek = tf_idMekanik.getText();
            String idBar = t_idBarang.getText();
            String tgl = t_tgService.getText();
            Long harga = Long.valueOf(tf_hargaService.getText());
            Long total = Long.valueOf(lb_total.getText());
            String ket = tf_keterangan.getText();

            model_service sv = new model_service();
            model_mekanik mk = new model_mekanik();
            model_barangcus bc = new model_barangcus();

            sv.setKode_service(kode);
            mk.setId_mekanik(idMek);
            bc.setKode_barang(idBar);
            try {
                // Parse the string date to java.util.Date
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy"); // Replace "your_date_format" with the actual date format
                Date tgldate = sdf.parse(tgl);
                sv.setTgl_service(tgldate); // Corrected line
            } catch (ParseException e) {
                e.printStackTrace(); // Handle the parsing exception appropriately
                return; // Return or handle error accordingly
            }
            sv.setHarga_service(harga);
            sv.setTotal(total);
            sv.setKeterangan(ket);
            sv.setStatus("Proses");

            sv.setMoMek(mk);
            sv.setBarangcus(bc);

            tblModel.tambahData(sv);
            servis.tambahData(sv);  
    }
    
    private void simpatDet() {
        DefaultTableModel tb = (DefaultTableModel) tbl_sementara.getModel();
        int rowCount = tb.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            model_detailService d = new model_detailService();
            model_service s = new model_service();
            model_onderdil mod = new model_onderdil();

            String kode = t_kodeService.getText();
            String kodeOnderdil = tb.getValueAt(i, 0).toString();
            String namaOnderdil = tb.getValueAt(i, 1).toString();
            String harga = tb.getValueAt(i, 2).toString();
            String jumlah = tb.getValueAt(i, 3).toString();
            String subTotal = tb.getValueAt(i, 4).toString();

            s.setKode_service(kode);
            mod.setKode_onderdil(kodeOnderdil);
            mod.setNama_onderdil(namaOnderdil);
            mod.setHarga_onderdil(Long.valueOf(harga));
            d.setJmh_service(Integer.valueOf(jumlah));
            d.setSubtotal(Long.valueOf(subTotal));

            d.setModService(s);
            d.setModOnd(mod);

            servis_det.tambahData(d);
        }
        loadData();
        resetForm();
        tampilPanel();
        btn_tambah1.setText("TAMBAH");
    }


