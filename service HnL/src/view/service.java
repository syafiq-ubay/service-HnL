    private boolean cekStok(String kodeOnderdil, int jmh) {
        boolean stokCukup = false;
        PreparedStatement st = null;
        ResultSet rs = null;

        try {
            //query untuk mengambil data stok dari onderdil
            String sql = "SELECT stok FROM onderdil WHERE kode_onderdil = ?";
            st = conn.prepareStatement(sql);
            st.setString(1, kodeOnderdil);
            rs = st.executeQuery();

            if (rs.next()) {
                //ambil jumlah stok onderdil dari database
                int stok = rs.getInt("stok");

                if (stok >= jmh) {
                    stokCukup = true;
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            stokCukup = false;
    } finally {
        try {
            if (st != null) st.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    return stokCukup;
}

   
