    private boolean validasiIn() {
        boolean valid = false;
        if (t_kodeService.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode Supply Onderdil tidak boleh kosong");
        } else if (t_tgService.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tgl Supply tidak boleh kosong");
        } else if (lb_total.getText().trim().isEmpty() || lb_total.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "total tidak boleh 0");
        } else if (tf_idMekanik.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "ID Mekanik tidak boleh kosong");
        } else if (tf_hargaService.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Harga Service tidak boleh kosong");
        } else if (t_idBarang.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "ID Barang Customer tidak boleh kosong");
        } else if (tf_keterangan.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Keterangan tidak boleh kosong");
        } else {
            valid = true;
        }
        return valid;
    }

    
