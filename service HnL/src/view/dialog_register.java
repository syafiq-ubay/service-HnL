    private void add() {
        if(validasiInput()==true){
        String kode = tf_idAdmin.getText();
        String nama = tf_nama.getText();
        String user = tf_user.getText();
        String pass = tf_pass.getText();

        model_admin mod = new model_admin();

        mod.setId_admin(kode);
        mod.setNama_admin(nama);
        mod.setUsername(user);
        mod.setPassword(pass);

        servis.tambahData(mod);
        }
    }

    private void clear() {
        tf_idAdmin.setText("");
        tf_nama.setText("");
        tf_user.setText("");
        tf_pass.setText("");
    }

    private boolean validasiInput() {
     boolean valid = false;
     if(tf_idAdmin.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(null, "Kode Jenis tidak boleh kosong");
    }else if (tf_nama.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(null, "Nama Jenis tidak boleh kosong");
    }else if (tf_user.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(null, "Tolong untuk membuat Username");
    }else if (tf_pass.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(null, "Tolong untuk membuat Password");
    }else{
        valid = true;
    }
     return valid;
    }
