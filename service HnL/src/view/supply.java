    private void simpanData() {

        if (validasiIn() == true) {
            String kode = t_kodeSupply.getText();
            String tgl = t_tglSupply.getText();
            Long total = Long.valueOf(lb_total.getText());
            String sup = t_idSupplier.getText();

            model_supply sp = new model_supply();
            model_supplier spr = new model_supplier();

            sp.setKode_supply(kode);
            try {
                // Parse the string date to java.util.Date
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy"); // Replace "your_date_format" with the actual date format
                Date tgldate = sdf.parse(tgl);
                sp.setTgl_supply(tgldate); // Corrected line
            } catch (ParseException e) {
                e.printStackTrace(); // Handle the parsing exception appropriately
                return; // Return or handle error accordingly
            }
            sp.setTotal_supply(total);
            spr.setId_supplier(sup);
            sp.setStatus("Pending");

            sp.setSupplier(spr);

            tblModel.tambahData(sp);
            servis.tambahData(sp);
        }
        
    }
    
    private void simpatDet() {
        if (validasiIn()==true) {
            DefaultTableModel tb = (DefaultTableModel) tbl_sementara.getModel();
            int rowCount = tb.getRowCount();

            for (int i = 0; i < rowCount; i++) {
                model_detailSupply d = new model_detailSupply();
                model_supply s = new model_supply();
                model_onderdil mod = new model_onderdil();

                String kode = t_kodeSupply.getText();
                String kodeOnderdil = tb.getValueAt(i, 0).toString();
                String namaOnderdil = tb.getValueAt(i, 1).toString();
                String harga = tb.getValueAt(i, 2).toString();
                String jumlah = tb.getValueAt(i, 3).toString();
                String subTotal = tb.getValueAt(i, 4).toString();

                s.setKode_supply(kode);
                mod.setKode_onderdil(kodeOnderdil);
                mod.setNama_onderdil(namaOnderdil);
                mod.setHarga_onderdil(Long.valueOf(harga));
                d.setJmh_supply(Integer.valueOf(jumlah));
                d.setSubtotal_detailsupply(Long.valueOf(subTotal));

                d.setOnderdil(mod);
                d.setSupply(s);
                
                servis_det.tambahData(d);
            }
            
        }
    }


