    private boolean validasiIn() {
        boolean valid = false;
        if (t_kodeSupply.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode Supply Onderdil tidak boleh kosong");
        } else if (t_tglSupply.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tgl Supply tidak boleh kosong");
        } else if (lb_total.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Harus Ada Onderdil Yang di Pesan");
        } else if (t_idSupplier.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "ID Supplier tidak boleh kosong");
        
        } else {
            valid = true;
        }
        return valid;
    }
    
